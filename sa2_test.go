package sa2_test

import (
	sa2 "github.com/jpbede/sa2go"
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestSA2_Execute(t *testing.T) {
	sa := sa2.New([]byte{0x68, 0x03, 0x81, 0x93, 0x54, 0x41, 0x4A, 0x49, 0x4A, 0x05, 0x87, 0x53, 0x41, 0x4A, 0x49, 0x49, 0x4C})
	assert.Equal(t, 0xe376c3c5, sa.Execute(0x06060607))

	sa = sa2.New([]byte{0x68, 0x02, 0x81, 0x4A, 0x10, 0x68, 0x04, 0x93, 0x08, 0x08, 0x20, 0x09, 0x4A, 0x05, 0x87, 0x22, 0x12, 0x19, 0x54, 0x82, 0x49, 0x93, 0x07, 0x12, 0x20, 0x11, 0x82, 0x4A, 0x05, 0x87, 0x03, 0x11, 0x20, 0x10, 0x82, 0x4A, 0x01, 0x81, 0x49, 0x4C})
	assert.Equal(t, 0x3C7876D8, sa.Execute(0xa04eb1ed))

	sa = sa2.New([]byte{0x68, 0x05, 0x81, 0x4A, 0x05, 0x87, 0x5F, 0xBD, 0x5D, 0xBD, 0x49, 0x4C})
	assert.Equal(t, 0xC1393A1C, sa.Execute(0x0CF4233D))
}
